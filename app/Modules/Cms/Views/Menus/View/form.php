//[Services]-----------------------------------------------------------------------------
$request = service('Request');
$bootstrap = service('Bootstrap');
$dates = service('Dates');
$strings = service('strings');
$authentication = service('authentication');
$f = service("forms", array("lang" => "Cms_Menus."));
//[Request]-----------------------------------------------------------------------------
$row = $model->get_Menus($oid);
$r["menu"] = $row["menu"];
$r["post"] = $row["post"];
$r["men_menu"] = $row["men_menu"];
$r["title"] = $row["title"];
$r["description"] = $row["description"];
$r["parent"] = $row["parent"];
$r["target"] = $row["target"];
$r["author"] = $row["author"];
$r["created_at"] = $row["created_at"];
$r["updated_at"] = $row["updated_at"];
$r["deleted_at"] = $row["deleted_at"];
$back = "/cms/menus/list/" . lpk();
//[Fields]-----------------------------------------------------------------------------
$f->fields["menu"] = $f->get_FieldView("menu", array("value" => $r["menu"], "proportion" => "col-xl-4 col-lg-4 col-md-4 col-sm-12 col-12"));
$f->fields["post"] = $f->get_FieldView("post", array("value" => $r["post"], "proportion" => "col-xl-4 col-lg-4 col-md-4 col-sm-12 col-12"));
$f->fields["men_menu"] = $f->get_FieldView("men_menu", array("value" => $r["men_menu"], "proportion" => "col-xl-4 col-lg-4 col-md-4 col-sm-12 col-12"));
