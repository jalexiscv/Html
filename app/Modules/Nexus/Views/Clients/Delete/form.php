//[services]------------------------------------------------------------------------------------------------------------
$b = service("bootstrap");
//[models]--------------------------------------------------------------------------------------------------------------
$model = model("App\Modules\Application\Models\Application_Clients");
//[vars]----------------------------------------------------------------------------------------------------------------
$f = service("forms", array("lang" => "Clients."));
$r = $model->find($oid);
$name = urldecode($r["name"]);
$message = sprintf(lang("Application.clients-delete-message"), $name);
$back = "/nexus/clients/list/" . lpk();
/*
 * -----------------------------------------------------------------------------
 * [Fields]
 * -----------------------------------------------------------------------------
*/
