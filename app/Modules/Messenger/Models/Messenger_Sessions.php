namespace app\Messenger\Models;

use App\Modules\Messenger\Models\type;
use Higgs\Model;
use Config\Database;

class Messenger_Sessions extends Model
{

    protected $table = "messenger_sessions";
    protected $primaryKey = "session";
    protected $returnType = "array";
    protected $useSoftDeletes = true;
    protected $allowedFields = [
        "Authentication",
        "user",
        "count",
        "created_at",
        "updated_at",
        "deleted_at",
    ];
    protected $useTimestamps = true;
    protected $createdField = "created_at";
    protected $updatedField = "updated_at";
    protected $deletedField = "deleted_at";
    protected $validationRules = [];
    protected $validationMessages = [];
    protected $skipValidation = false;
    protected $DBGroup = "authentication"; //default

    /**
     * Inicializa el modelo y la regeneración de la tabla asociada si esta no existe
     * */
    public function __construct()
    {
        parent::__construct();
        $this->regenerate();
    }

    /**
     * Regenera o recrea la tabla de la base de datos en caso de que esta no exista
     * un mes de un año especifico.
     * @param type $year
     * @param type $month
     */
    public function regenerate()
    {
        $forge = Database::forge($this->DBGroup);
        $fields = [
            'session' => ['type' => 'VARCHAR', 'constraint' => 128, 'null' => FALSE],
            'user' => ['type' => 'VARCHAR', 'constraint' => 13, 'null' => FALSE],
            'count' => ['type' => 'INT', 'constraint' => 10, 'default' => '0', 'null' => FALSE],
            'created_at' => ['type' => 'TIMESTAMP', 'null' => TRUE],
            'updated_at' => ['type' => 'TIMESTAMP', 'null' => TRUE],
            'deleted_at' => ['type' => 'TIMESTAMP', 'null' => TRUE],
        ];
        $forge->addField($fields);
        $forge->addPrimaryKey($this->primaryKey);
        $forge->addKey('user');
        $forge->createTable($this->table, TRUE);
    }

    /**
     * Retorna el listado de elementos existentes
     * un mes de un año especifico.
     * @param type $year
     * @param type $month
     */
    public function get_List()
    {
        $sql = "SELECT * FROM `{$this->table}` ORDER BY `reference` ASC;";
        $query = $this->db->query($sql);
        $result = $query->getResultArray();
        if (is_array($result)) {
            return ($result);
        } else {
            return (false);
        }
    }

    /**
     * Retorna falso o verdadero si el usuario activo ne la sesión es el
     * autor del regsitro que se desea acceder, editar o eliminar.
     * @param type $id codigo primario del registro a consultar
     * @param type $author codigo del usuario del cual se pretende establecer la autoria
     * @return boolean falso o verdadero segun sea el caso
     */
    public function get_Authority($id, $author)
    {
        $row = $this->where($this->primaryKey, $id)->first();
        if (@$row["author"] == $author) {
            return (true);
        } else {
            return (false);
        }
    }

    /**
     * Retorna el listado de elementos existentes
     * un mes de un año especifico.
     * @param type $year
     * @param type $month
     */
    public function get_SelectData()
    {
        $sql = "SELECT `service` AS `value`,`name` AS `label` FROM `{$this->table}` ORDER BY `service` ASC;";
        //$sql = "SELECT *, `reference` AS `value`,CONCAT_WS(': ', `reference`, `name`) AS `label` FROM `{$this->table}` ORDER BY `reference` ASC;";
        $query = $this->db->query($sql);
        $result = $query->getResultArray();
        if (is_array($result)) {
            return ($result);
        } else {
            return (false);
        }
    }

}

