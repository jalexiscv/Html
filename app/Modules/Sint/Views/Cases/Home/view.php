$bootstrap = service("bootstrap");

$mcases = model("App\Modules\Sint\Models\Sint_Cases");
$cases = $mcases->get_List(1000, 0);

$shortcuts = $bootstrap->get_Shortcuts(array("id" => "shortcuts-panel"));
$shortcuts->add($bootstrap->get_Shortcut(array(
        "href" => "/sint/cases/create/" . lpk(),
        "icon" => ICON_PLUS,
        "value" => "Crear Caso",
        "description" => "Nuevo")
));


if ($authentication->get_LoggedIn() && $authentication->has_Permission("sint-cases-access")) {
    foreach ($cases as $case) {
        $shortcuts->add($bootstrap->get_Shortcut(array(
            "href" => "/sint/cases/view/" . $case['case'],
            "icon" => ICON_CASE,
            "value" => $case['title'],
            "description" => $case['description']
        )));
    }
    echo($shortcuts);
}


