$code = "";

//[models]---------------------------------------------------------------------------------------------------------------
$mdatabreaches = model("App\Modules\Databreaches\Models\Databreaches_Mongo");
//[file]---------------------------------------------------------------------------------------------------------------
$file = $request->getFile($f->get_fieldId('file'));
if (!is_null($file) && $file->isValid()) {
    $rname = $file->getRandomName();
    $name = $file->getClientName();
    $type = $file->getClientMimeType();
    $content = $file->openFile()->fread($file->getSize());
    $lines = explode("\n", $content);
    $count = 0;
    foreach ($lines as $line) {
        if (strlen($line) < 64) {
            $data = explode(":", $line);
            $email = @$data[0];
            $password = @$data[1];
            if (!empty($email) && !empty($password)) {
                $count++;
                $filter = [
                    'email' => new MongoDB\BSON\Regex($email, 'i'),
                    'password' => new MongoDB\BSON\Regex($password, 'i')
                ];
                $exist = $mdatabreaches->findAll(10, 0, $filter);
                if (count($exist) > 0) {
                    $code .= $count . ": " . $line . " - " . strlen($line) . " - [EXISTE]<br>";
                    continue;
                }
                $mdatabreaches->insert(array(
                    'breach' => $breach,
                    'email' => $email,
                    'password' => $password
                ));

                $code .= $count . ": " . $line . " - " . strlen($line) . "- [REGISTRADO]<br>";
            }
        }
    }
} else {

