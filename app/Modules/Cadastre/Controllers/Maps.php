use App\Controllers\ModuleController;

class Maps extends ModuleController
{

    public function __construct()
    {
        parent::__construct();
        $this->prefix = 'cadastre-maps';
        $this->module = 'App\Modules\Cadastre';
        $this->views = $this->module . '\Views';
        $this->viewer = $this->views . '\index';
        helper($this->module . '\Helpers\Cadastre');
    }

    public function index()
    {
        $url = base_url('cadastre/profiles/home/' . lpk());
        return (redirect()->to($url));
    }


    public function home(string $rnd)
    {
        $this->oid = $rnd;
        $this->prefix = "{$this->prefix}-home";
        $this->component = $this->views . '\Maps\Home';
        return (view($this->viewer, $this->get_Array()));
    }

    public function routes(string $rnd)
    {
        $this->oid = $rnd;
        $this->prefix = "{$this->prefix}-routes";
        $this->component = $this->views . '\Maps\Routes';
        return (view($this->viewer, $this->get_Array()));
    }


}

