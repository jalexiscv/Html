use App\Controllers\ModuleController;

class Services extends ModuleController
{


    public function __construct()
    {
        parent::__construct();
        $this->prefix = 'c4isr-services';
        $this->module = 'App\Modules\C4isr';
        $this->views = $this->module . '\Views';
        $this->viewer = $this->views . '\index';
        helper($this->module . '\Helpers\C4isr');
    }

    public function index()
    {
        $url = base_url('c4isr/incidents/home/' . lpk());
        return (redirect()->to($url));
    }


    public function home(string $rnd)
    {
        $this->oid = $rnd;
        $this->prefix = "{$this->prefix}-home";
        $this->component = $this->views . '\Services\Home';
        return (view($this->viewer, $this->get_Array()));
    }

    public function osint(string $oid)
    {
        $this->oid = $oid;
        $this->prefix = "{$this->prefix}-osint";
        $this->component = $this->views . '\Services\Osint';
        return (view($this->viewer, $this->get_Array()));
    }

}

