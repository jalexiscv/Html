use App\Controllers\ModuleController;

class Physicals extends ModuleController
{
    public function __construct()
    {
        parent::__construct();
        $this->prefix = 'c4isr-physicals';
        $this->module = 'App\Modules\C4isr';
        $this->views = $this->module . '\Views';
        $this->viewer = $this->views . '\index';
        helper($this->module . '\Helpers\C4isr');
    }

    public function index()
    {
        $url = base_url('c4isr/physicals/home/' . lpk());
        return (redirect()->to($url));
    }


    public function home(string $rnd)
    {
        $this->oid = $rnd;
        $this->prefix = "{$this->prefix}-home";
        $this->component = $this->views . '\Physicals\Home';
        return (view($this->viewer, $this->get_Array()));
    }

