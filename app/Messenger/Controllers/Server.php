use App\Controllers\ModuleController;
use App\Libraries\Ratchet;


class Server extends ModuleController
{
    public function __construct()
    {
        parent::__construct();
        $this->prefix = 'messenger-chat';
        $this->module = 'App\Modules\Messenger';
        $this->views = $this->module . '\Views';
        $this->viewer = $this->views . '\index';
        helper($this->module . '\Helpers\Messenger');
    }

    public function start($oid)
    {

        $ws = service('websocket');
        $ws->set_CallBack('auth', array($this, '_auth'));
        $ws->set_CallBack('event', array($this, '_event'));
        $ws->run();
    }


}

